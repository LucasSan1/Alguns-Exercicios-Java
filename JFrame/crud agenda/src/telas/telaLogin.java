     public void logar() {;
        String sql = "SELECT * FROM usuario WHERE email =? OR nome = ? AND senha =?";
        try{
            //Capturar o que o usuario digitar na cx de txtUsuario e txtSenha
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2,txtUsuario.getText());
            pst.setString(3, txtSenha.getText());

            //Execulta a consulta
            rs = pst.executeQuery();
            //Se existir usuario e senha no db
            if(rs.next()) {
                telaPrincipal principal = new telaPrincipal();
                principal.setVisible(true);
             
                //Fecha a tela de login
                this.dispose();
                conexao.close();
            } else{
                JOptionPane.showMessageDialog(null, "Usuario n√£o encontrado", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

